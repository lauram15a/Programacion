    private void botonIniciarSesion_IniciarSesionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonIniciarSesion_IniciarSesionMouseClicked
        // TODO add your handling code here:
        String dniString = textFieldDNI_IniciarSesion.getText();
        if (Usuario.comprobarDNI(dniString) == false) {
            JOptionPane.showMessageDialog(this, "DNI FALSO");
            textFieldDNI_IniciarSesion.setText("");
        } else {
            if (Torneo.comprobarUsuarioRegistrado(torneo, dniString)) { //ususario registrado
                Historial historial = Torneo.devolverHistorial(torneo, dniString);
                SesionIniciada sI = new SesionIniciada(torneo, historial.getUsuario());
                sI.setVisible(true);
                this.dispose();
            } else { //usuario no registrado
                JOptionPane.showMessageDialog(this, "Usuario no registrado.");
                textFieldDNI_IniciarSesion.setText("");
            }
        }
    }//GEN-LAST:event_botonIniciarSesion_IniciarSesionMouseClicked

