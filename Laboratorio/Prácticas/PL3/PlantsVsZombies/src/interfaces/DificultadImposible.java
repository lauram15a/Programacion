    public DificultadImposible(HashMap<String, clases.Historial> torne, Usuario usuari) {
        torneo = torne;
        usuario = usuari;
        initComponents();
        transparenciaBoton();
        this.setLocationRelativeTo(null);  //ajustarlo en el medio de la pantalla

        Historial historial = Torneo.devolverHistorial(torneo, usuario.getDNI());
        jLabelNombre_DificultadImposible.setText(usuario.getNombre());
        jLabelPuntos_DificultadImposible.setText(Integer.toString(historial.getPartidasImposibles().getPuntos()));

        PartidaBaja p = historial.getPartidasBajas();
        ArrayList<Partida> array = p.getPartidasBajas();
        
        //creamos las columnas de la tabla
        model.addColumn("Puntos");
        model.addColumn("Terminada");
        model.addColumn("Ganada");
        this.jTable_DificultadImposible.setModel(model);

        for (int i = 0; i < array.size(); i++){
            String[] fila = new String[3];
            fila[0] = Integer.toString(array.get(i).getPuntos());
            fila[1] = Boolean.toString(array.get(i).isTerminada());
            fila[2] = Boolean.toString(array.get(i).isGanada());
            model.addRow(fila);
            
        }
        
        setIconImage(new ImageIcon(getClass().getResource("/imagenes/icono.jpg")).getImage());  //cambiar logo
    }

