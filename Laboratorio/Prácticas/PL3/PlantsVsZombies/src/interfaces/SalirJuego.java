    private void botonGuardarPartida_SalirJuegoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonGuardarPartida_SalirJuegoMouseClicked
        // TODO add your handling code here:
        Historial historial = Torneo.devolverHistorial(torneo, usuario.getDNI());
        Partida partida = new Partida(puntos[0], terminada, ganada, tablero);
        if (dificultad == "BAJA"){
            historial.getPartidasBajas().getPartidasBajas().add(partida);
        } else if (dificultad == "MEDIA"){
            historial.getPartidasMedias().getPartidasMedias().add(partida);
        } else if (dificultad == "ALTA"){
            historial.getPartidasAltas().getPartidasAltas().add(partida);
        } else if (dificultad == "IMPOSIBLE"){
            historial.getPartidasImposibles().getPartidasImposibles().add(partida);
        } 
        
        IniciarSesion inicioSesion = new IniciarSesion();
        inicioSesion.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_botonGuardarPartida_SalirJuegoMouseClicked

