    public static boolean esBisiesto(int y) {
        return (y % 4 == 0) && ((y % 100 != 0) || (y % 400 == 0));
    }

    public static boolean buenaFecha(int d, int m, int a) {
        int[] diasMes = {31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
        if (esBisiesto(a)) {
            diasMes[1] = 29; //Febrero es el segundo mes (Ã­ndice 1)
        }
        return (a > 0 && a < 3000 && m >= 1 && m <= 12 && d >= 1 && d <= diasMes[m - 1]);
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Tratamiento fecha jFormattedTextField
        String fecha1txt = (String) jFormattedTextField1.getValue();
        String[] partes = fecha1txt.split("/");
        int d1 = Integer.parseInt(partes[0]);
        int m1 = Integer.parseInt(partes[1]);
        int a1 = Integer.parseInt(partes[2]);
        if (!buenaFecha(d1, m1, a1)) {
            fecha1txt = "Fecha incorrecta";
        }
        jTextField1.setText(fecha1txt);
        
        // Tratamiento fecha jSpinner
        // De Date a LocalTime
        Date fecha2 = (Date) jSpinner1.getValue();
        Instant instant = Instant.ofEpochMilli(fecha2.getTime());
        LocalDate localDate = LocalDateTime.ofInstant(instant, ZoneId.systemDefault()).toLocalDate();
        DateTimeFormatter formatoCorto = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        jTextField2.setText(localDate.format(formatoCorto));
    }//GEN-LAST:event_jButton1ActionPerformed

